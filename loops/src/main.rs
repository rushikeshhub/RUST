fn main() {
    println!("Hello, world!");
    // If loop 
    let a = true;
    if a {
        println!(" A is True");
    }else
    {
        println!("B is True");
    }

    // while loop

    let mut b = 1;
    while b < 5 {
            println!("{b}");
            b += 1;
    }

    // For loop

   let c = [6,7,8,9];

   for i in c {
       println!("{i}");
    }

}
